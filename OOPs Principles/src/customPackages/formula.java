package customPackages;

public class formula {

        public void AreaOfCircle ( int radius){
        System.out.println("Area of Circle :" + 3.14 * radius * radius);
        }
        void AreaOfTriangle ( int height, int breadth){
        System.out.println("Area of Triangle:" + 0.5 * height * breadth);
        }
        void AreaOfSquare ( int Side){
        System.out.println("Area of Square:" + Side * Side);
        }
        void AreaOfRectangle ( int length, int breadth){
        System.out.println("Area of Rectangle:" + length * breadth);
        }
}
